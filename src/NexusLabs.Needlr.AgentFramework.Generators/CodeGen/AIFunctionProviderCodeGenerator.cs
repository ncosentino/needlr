        sb.AppendLine($"            arguments.TryGetValue(\"{param.Name}\", out var {rawVar});");

        switch (param.JsonSchemaType)
        {
            case "string":
                sb.AppendLine($"            var {param.Name} = {rawVar} is global::System.Text.Json.JsonElement {jVar} ? {jVar}.GetString() ?? \"\" : {rawVar}?.ToString() ?? \"\";");
                break;
